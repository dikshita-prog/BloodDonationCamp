    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
          String d_name=name.getText();
          int d_age=Integer.parseInt(age.getText());
          String d_city=city.getText();
          String d_bg=(String)bg.getSelectedItem();
          String comm=comments.getText();
         Class.forName("com.mysql.jdbc.Driver");
         Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/users","root","password");
         String sql = "Insert into donors(name,age,city,bloodgroup,medicalhistory) VALUES (?,?,?,?,?)";
         PreparedStatement pst = con.prepareStatement(sql);
         pst.setString(1,d_name);
         pst.setInt(2,d_age);
         pst.setString(3,d_city);
         pst.setString(4,d_bg);
         pst.setString(5,comm);
         int i=pst.executeUpdate();
         if(i>0){
            JOptionPane.showMessageDialog(null,"Successfully inserted!");
             HomePage hp=new HomePage(d_name);
             hp.setVisible(true);
             setVisible(false);
        }   
    }catch(Exception e){
       JOptionPane.showMessageDialog(null,e);
      }
    }//GEN-LAST:event_jButton1ActionPerformed

