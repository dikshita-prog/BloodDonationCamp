    private void nameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nameFocusLost
        // TODO add your handling code here:
String regx = "^[\\p{L} .'-]+$";
Pattern p = Pattern.compile(regx);        
if(name.getText().isEmpty()){
            name.requestFocus();
}
else if(!p.matcher(name.getText()).matches()){
    nameErr.setText("*Only characters and spaces allowed!");
    name.requestFocus();
}
else{
 nameErr.setVisible(false);
}
    }//GEN-LAST:event_nameFocusLost

    private void ageFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ageFocusLost
    if(age.getText().isEmpty()){
               age.requestFocus();
}
   else {
        try{
            int a=Integer.parseInt(age.getText());
            if(a<18 || a>100){
                ageErr.setText("*Not elligible");
                age.requestFocus();
            }
            else{
                ageErr.setVisible(false);
            }
        }
        catch(NumberFormatException e){
            ageErr.setText("*Invalid input");
            age.requestFocus();
            }
        }
    }//GEN-LAST:event_ageFocusLost

    private void cityFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cityFocusLost
        if(city.getText().isEmpty()){
              city.requestFocus();
    }
    String regx = "^[\\p{L} .'-]+$";
    Pattern p = Pattern.compile(regx);
    if(!p.matcher(city.getText()).matches()){
         cityErr.setText("*Invalid Input");
         city.requestFocus();
    }
    else
        cityErr.setVisible(false);
    }//GEN-LAST:event_cityFocusLost

